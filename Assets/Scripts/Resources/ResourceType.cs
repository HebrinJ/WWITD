/// <summary>
/// Перечисление всех типов ресурсов, используемых в экономической системе игры.
/// Определяет валюты для внутриуровневой экономики и глобальной прокачки.
/// </summary>
public enum ResourceType
{
    /// <summary>
    /// Денежный ресурс. Основная валюта для внутриуровневых операций.
    /// Применение: строительство и улучшение большинства башен, активация способностей.
    /// Источники: награда за убийство врагов, завершение волн.
    /// </summary>
    Money,

    /// <summary>
    /// Железо. Специализированный ресурс для тяжелых сооружений.
    /// Применение: строительство и улучшение тяжелых башен (артиллерия, ПТО, ПВО), инженерных сооружений.
    /// Источники: добывается из «Заброшенных карьеров» на уровнях.
    /// </summary>
    Iron,

    /// <summary>
    /// Горючее. Специализированный ресурс для огнеметов и способностей.
    /// Применение: строительство и улучшение огнеметов, активация некоторых тактических способностей (авиаудар).
    /// Источники: добывается из «Нефтяных вышек» на уровнях.
    /// </summary>
    Fuel,

    /// <summary>
    /// Чертежи. Глобальный ресурс для исследований между уровнями.
    /// Применение: исследования в древе технологий (новые башни, улучшения, способности).
    /// Источники: награда за прохождение уровней, выполнение дополнительных заданий, с шансом выпадает с боссов.
    /// Особенность: не используется непосредственно на уровнях, только в межуровневом меню.
    /// </summary>
    Blueprints,
}
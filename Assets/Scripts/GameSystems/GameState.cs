/// <summary>
/// Перечисление, определяющее глобальные состояния игрового процесса.
/// Используется для управления логикой, зависимой от состояния игры.
/// </summary>
public enum GameState
{
    /// <summary>
    /// Состояние главного меню. Игрок не находится на карте или уровне.
    /// В этом состоянии отображается главный UI меню.
    /// </summary>
    MainMenu,

    /// <summary>
    /// Состояние стратегической карты кампании. Игрок выбирает уровень для прохождения или проводит исследования.
    /// В этом состоянии управление камерой и UI карты активно, тактический геймплей отсутствует.
    /// </summary>
    StrategyMap,

    /// <summary>
    /// Состояние тактического уровня. Непосредственно активный бой, строительство башен и защита штаба.
    /// В этом состоянии активны все менеджеры уровня (WaveManager, BuildManager и дургие), работает игровая логика.
    /// </summary>
    TacticalLevel,

    /// <summary>
    /// Состояние паузы. Игровой процесс приостановлен, независимо от текущего экрана (карта или уровень).
    /// Время (Time.timeScale) установлено в 0. Большинство input'ов заблокировано, отображается UI паузы.
    /// </summary>
    Paused
}

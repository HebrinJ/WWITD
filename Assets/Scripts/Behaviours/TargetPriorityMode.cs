
/// <summary>
/// Режимы приоритета выбора целей для башен.
/// Определяет алгоритм, по которому башня выбирает врагов для атаки.
/// </summary>
/// <remarks>
/// <para><b>Closest:</b> Башня атакует ближайшего врага в радиусе действия.</para>
/// <para><b>Priority:</b> Башня атакует врагов по приоритету (сильные, быстрые и т.д.).</para>
/// </remarks>
public enum TargetPriorityMode
{
    Closest,
    Priority
}
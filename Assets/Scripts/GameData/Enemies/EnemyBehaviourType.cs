/// <summary>
/// Определяет тип поведения вражеского юнита на карте.
/// Влияет на выбор целей и реакцию на угрозы со стороны башен игрока.
/// </summary>
public enum EnemyBehaviourType
{
    /// <summary>
    /// Агрессивный тип поведения.
    /// Юнит атакует не только штаб, но и оборонительные сооружения игрока.
    /// При получении урона от башни переключается на нее и ведет ответный огонь.
    /// </summary>
    agressive,

    /// <summary>
    /// Пассивный тип поведения.
    /// Юнит игнорирует башни игрока и движется прямо к штабу.
    /// Атакует только штаб при приближении к нему.
    /// </summary>
    passive,

    /// <summary>
    /// Отсутствие поведения (специальный тип).
    /// Может использоваться для нейтральных юнитов, тестовых целей
    /// или как значение по умолчанию при инициализации.
    /// </summary>
    none
}
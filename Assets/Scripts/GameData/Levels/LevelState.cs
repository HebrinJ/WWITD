
/// <summary>
/// Состояние уровня на стратегической карте.
/// Определяет визуальное представление и доступность уровня.
/// </summary>
public enum LevelState
{
    /// <summary>
    /// Уровень заблокирован и недоступен для выбора.
    /// </summary>
    Locked,

    /// <summary>
    /// Уровень доступен для прохождения.
    /// </summary>
    Available,

    /// <summary>
    /// Уровень успешно пройден.
    /// </summary>
    Completed
}
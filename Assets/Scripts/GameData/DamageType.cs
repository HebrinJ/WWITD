/// <summary>
/// Перечисление всех типов урона, существующих в боевой системе игры.
/// Определяет механику расчета урона, взаимодействие с броней и визуальные/звуковые эффекты.
/// Используется как в <see cref="TowerDataSO"/>, так и в <see cref="EnemyDataSO"/>.
/// </summary>
public enum DamageType
{
    /// <summary>
    /// Пулевой урон. Стандартный тип урона для стрелкового оружия.
    /// Механика: Наносит полный урон здоровью небронированных целей.
    /// Против брони: Наносит 1 ед. урона броне за атаку (не эффективен).
    /// </summary>
    bullet,

    /// <summary>
    /// Бронебойный урон. Специализированный тип урона против брони.
    /// Механика: Наносит одинаковый урон как броне, так и здоровью цели одновременно.
    /// Эффективен против: бронетехники, укрепленных сооружений.
    /// </summary>
    piercing,

    /// <summary>
    /// Огненный урон. Наносит периодический урон и может накладывать эффекты.
    /// Механика: Наносит прямой урон + накладывает эффект "Горение" (DoT).
    /// Эффект "Горение": Наносит периодический урон в течение времени.
    /// Против брони: Наносит 1 ед. урона броне за атаку.
    /// </summary>
    fire,

    /// <summary>
    /// Взрывной урон. Наносит урон по площади с эпицентром и затуханием к краям.
    /// Механика: Наносит полный урон в эпицентре и уменьшающийся к краям радиуса.
    /// Против брони: Наносит 1 ед. урона броне за атаку (не эффективен).
    /// Особенность: Может наносить урон нескольким целям одновременно.
    /// </summary>
    explode,

    /// <summary>
    /// Противовоздушный урон. Специализированный тип против воздушных целей.
    /// Механика: Наносит полный урон воздушным целям.
    /// Особенность: Не может наносить урон наземным целям.
    /// </summary>
    antiair,

    /// <summary>
    /// Отсутствие урона. Специальное значение.
    /// Используется для: нейтральных объектов, тестовых целей, состояний без урона.
    /// </summary>
    none
}
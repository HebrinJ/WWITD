/// <summary>
/// Перечисление всех доступных типов оборонительных сооружений (башен) в игре.
/// Каждый тип соответствует уникальной боевой единице с определенной ролью и характеристиками.
/// Используется для идентификации башен в системах строительства, улучшения и управления.
/// </summary>
public enum TowerType
{
    /// <summary>
    /// Стрелки в окопе. Дешёвая противопехотная башня.
    /// Характеристики: низкая стоимость, высокая скорострельность, низкий урон.
    /// Эффективна против: пехоты.
    /// </summary>
    Riflemen,

    /// <summary>
    /// Пулемётное гнездо. Скорострельная противопехотная башня.
    /// Характеристики: средняя стоимость, высокая скорострельность, высокий урон.
    /// Эффективна против: групп пехоты.
    /// </summary>
    Gunner,

    /// <summary>
    /// Снайперская вышка.
    /// Характеристики: высокий урон по одиночной цели, низкая скорострельность, большая дальность.
    /// Эффективна против: элитной пехоты.
    /// </summary>
    Marksman,

    /// <summary>
    /// Огнемёт. Башня для поражения на короткой дистанции с наложением DoT.
    /// Характеристики: низкая дальность, DoT-урон.
    /// Эффективна против: пехоты, групп юнитов.
    /// Требует: деньги + горючее.
    /// </summary>
    Flammer,

    /// <summary>
    /// Миномёт. Дальнобойная артиллерия.
    /// Характеристики: дальнобойный, AoE-урон, средняя скорострельность.
    /// Эффективна против: скоплений пехоты.
    /// </summary>
    Mortar,

    /// <summary>
    /// Противотанковое орудие (ПТО). Специализированная анти-броневая башня.
    /// Характеристики: очень высокий урон по броне, низкая скорострельность.
    /// Эффективна против: бронетехники, тяжелых танков.
    /// Требует: деньги + железо.
    /// </summary>
    Antitank,

    /// <summary>
    /// Гаубица. Мощная артиллерийская установка.
    /// Характеристики: очень мощный AoE-урон, очень низкая скорострельность, высокая стоимость.
    /// Эффективна против: любых наземных целей, особенно плотных групп.
    /// Требует: деньги + железо.
    /// </summary>
    Howitzer,

    /// <summary>
    /// Зенитное орудие. Специализированная ПВО-башня.
    /// Характеристики: атакует только воздушные цели, высокая скорострельность.
    /// Эффективна против: самолетов, бомбардировщиков.
    /// Требует: деньги + железо.
    /// </summary>
    Antiaircraft,

    /// <summary>
    /// Специальное значение, обозначающее отсутствие типа башни.
    /// Используется по умолчанию, для инициализации или обозначения невыбранного типа.
    /// </summary>
    None
}
using UnityEngine;

/// <summary>
/// Структура данных, определяющая стоимость постройки или улучшения башни в одном конкретном ресурсе.
/// Используется как элемент массива в данных башни для описания комплексной стоимости.
/// </summary>
[System.Serializable]
public class TowerCost
{
    /// <summary>
    /// Тип ресурса, который требуется для постройки или улучшения.
    /// Определяет, какая валюта будет списана (деньги, железо, горючее).
    /// </summary>
    [Tooltip("Тип ресурса, который требуется затратить.")]
    public ResourceType resourceType;

    /// <summary>
    /// Количество единиц указанного ресурса, необходимое для операции.
    /// Должно быть положительным числом.
    /// </summary>
    [Tooltip("Количество единиц ресурса, необходимое для операции.")]
    public int amount;
}
using UnityEngine;

/// <summary>
/// Данные о группе однотипных врагов в рамках волны.
/// Определяет параметры спавна конкретного типа юнитов.
/// Сериализуется для настройки волн непосредственно в инспекторе Unity через <see cref="WaveDataSO"/>.
/// </summary>
[System.Serializable]
public class WaveEnemy
{
    /// <summary>
    /// Ссылка на ScriptableObject с параметрами врага.
    /// Определяет все характеристики спавнящегося юнита: здоровье, урон, скорость, поведение и т.д.
    /// </summary>
    [Tooltip("Data объекта врага. Определяет его характеристики и префаб.")]
    public EnemyDataSO enemyData;

    /// <summary>
    /// Общее количество юнитов данного типа в группе.
    /// Все враги этого типа будут созданы с указанным интервалом.
    /// </summary>
    [Tooltip("Общее количество юнитов этого типа в группе.")]
    public int count;

    /// <summary>
    /// Интервал между спавном отдельных юнитов в группе (в секундах).
    /// Например: значение 0.5 означает спавн 2 юнитов в секунду.
    /// Значение 0 приведет к мгновенному созданию всей группы (не рекомендуется).
    /// </summary>
    [Tooltip("Интервал между созданием каждого юнита в группе (в секундах).")]
    public float spawnInterval;
}
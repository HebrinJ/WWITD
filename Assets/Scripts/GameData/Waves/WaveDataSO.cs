using UnityEngine;

/// <summary>
/// ScriptableObject содержащий данные о одной волне врагов.
/// Определяет состав, количество и тайминги появления противников на уровне.
/// Используется системой <see cref="WaveManager"/> для управления спавном врагов.
/// </summary>
[CreateAssetMenu(fileName = "WaveDataSO", menuName = "Scriptable Objects/WaveDataSO")]
public class WaveDataSO : ScriptableObject
{
    /// <summary>
    /// Уникальный идентификатор волны для внутреннего использования в системе.
    /// Может использоваться для поиска, сохранения прогресса волны или отладки.
    /// Рекомендуется использовать понятные имена (например: "Level01_Wave03").
    /// </summary>
    [Tooltip("Уникальный ID волны для внутреннего использования (например: Level01_Wave03).")]
    public string waveId;

    /// <summary>
    /// Массив данных о группах врагов, которые будут появляться в этой волне.
    /// Каждая группа определяет тип врага, количество и интервал спавна.
    /// Группы обрабатываются последовательно в порядке массива.
    /// </summary>
    [Tooltip("Массив групп врагов в этой волне. Группы спавнятся последовательно.")]
    public WaveEnemy[] enemiesInWave;

    /// <summary>
    /// Задержка в секундах перед началом следующей волны после начала текущей.
    /// Если волна последняя на уровне, это значение игнорируется.
    /// </summary>
    [Tooltip("Задержка в секундах перед началом следующей волны после начала этой.")]
    public float nextWaveDelay;
}
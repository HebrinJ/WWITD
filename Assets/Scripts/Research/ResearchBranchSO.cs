using UnityEngine;

/// <summary>
/// ScriptableObject представляющий ветку исследований в древе технологий.
/// Группирует связанные исследования в логическую последовательность (например, ветка улучшений стрелковых башен).
/// Определяет порядок исследований внутри одной тематической ветки развития.
/// </summary>
[CreateAssetMenu(fileName = "New Research Branch", menuName = "Research System/Research Branch")]
public class ResearchBranchSO : ScriptableObject
{
    /// <summary>
    /// Отображаемое название ветки исследований в интерфейсе.
    /// Должно отражать тематику ветки (например: "Стрелковые башни", "Артиллерия", "Инженерия").
    /// Соответствует разделам из дизайн-документа: Пехота, Артиллерия, Инженерия, ПВО, Глобальные способности.
    /// </summary>
    [Tooltip("Название ветки исследований (например: 'Стрелковые башни', 'Артиллерия').")]
    public string BranchName;

    /// <summary>
    /// Массив узлов исследований, принадлежащих этой ветке, в порядке их исследования.
    /// Определяет последовательность прокачки внутри ветки.
    /// Узлы должны быть расположены в массиве в порядке их исследования: от базовых к продвинутым.
    /// </summary>
    [Tooltip("Узлы исследований в этой ветке, в порядке их изучения.")]
    public ResearchNodeSO[] ResearchNodes;
}
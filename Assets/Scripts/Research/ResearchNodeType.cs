/// <summary>
/// Перечисление типов узлов исследований в древе технологий.
/// Определяет категорию узла и его роль в системе прогрессии.
/// Соответствует архитектуре исследований из дизайн-документа: грейды и улучшения.
/// </summary>
public enum ResearchNodeType
{
    /// <summary>
    /// Узел-грейд. Представляет полную замену префаба башни на новый уровень.
    /// Грейды исследуются последовательно: Grade 0 → Grade 1 → Grade 2.
    /// Каждый грейд открывает новые улучшения для этой башни.
    /// Пример: Базовая стрелковая башня → Улучшенная стрелковая башня.
    /// </summary>
    Grade,

    /// <summary>
    /// Узел-улучшение. Представляет модификатор параметров существующей башни.
    /// Улучшения исследуются параллельно или последовательно в пределах грейда.
    /// Могут иметь цепочки: Upgrade 1 → Upgrade 2 → Upgrade 3.
    /// Пример: +10 к урону, +15% к дальности атаки.
    /// </summary>
    Upgrade
}
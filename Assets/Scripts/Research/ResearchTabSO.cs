using UnityEngine;

/// <summary>
/// ScriptableObject представляющий вкладку/раздел в интерфейсе исследований.
/// Группирует связанные ветки исследований по тематическим категориям.
/// Определяет визуальное представление и доступность раздела в UI.
/// </summary>
[CreateAssetMenu(fileName = "New Research Tab", menuName = "Research System/Research Tab")]
public class ResearchTabSO : ScriptableObject
{
    /// <summary>
    /// Тип вкладки. Определяет категорию и порядок отображения в интерфейсе.
    /// Соответствует разделам из дизайн-документа с прогрессивным открытием.
    /// </summary>
    [Tooltip("Тип вкладки (Башни, Инженерия, Способности).")]
    public ResearchTabType TabType;

    /// <summary>
    /// Отображаемое название вкладки в интерфейсе исследований.
    /// Должно кратко описывать категорию (например: "Башни", "Инженерия", "Способности").
    /// </summary>
    [Tooltip("Название вкладки для отображения в интерфейсе.")]
    public string TabName;

    /// <summary>
    /// Иконка вкладки для визуального представления в UI.
    /// Помогает игроку быстро идентифицировать категорию исследований.
    /// </summary>
    [Tooltip("Иконка вкладки для визуального представления.")]
    public Sprite TabIcon;

    /// <summary>
    /// Массив веток исследований, принадлежащих этой вкладке.
    /// Определяет полное содержимое раздела исследований.
    /// Ветки отображаются в указанном порядке в интерфейсе.
    /// </summary>
    [Tooltip("Ветки исследований, входящие в эту вкладку.")]
    public ResearchBranchSO[] BranchesInTab;

    /// <summary>
    /// Флаг, указывающий заблокирована ли вкладка изначально.
    /// Заблокированные вкладки неактивны в интерфейсе до выполнения условий разблокировки.
    /// Соответствует прогрессивному открытию разделов из дизайн-документа.
    /// </summary>
    [Tooltip("Блокировка вкладки до выполнения условий разблокировки.")]
    public bool IsLocked = false;
}
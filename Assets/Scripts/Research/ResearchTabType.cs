/// <summary>
/// Перечисление типов вкладок в интерфейсе исследований.
/// Определяет основные категории исследований для организации древа технологий.
/// Соответствует разделам из дизайн-документа с прогрессивным открытием.
/// </summary>
public enum ResearchTabType
{
    /// <summary>
    /// Вкладка исследований башен. Доступна с начала игры.
    /// Содержит ветки улучшений для всех типов оборонительных сооружений.
    /// Включает: стрелковые башни, артиллерию, ПВО и другие типы башен.
    /// </summary>
    Towers,

    /// <summary>
    /// Вкладка инженерных исследований. Открывается после прохождения уровня N.
    /// Содержит улучшения для инженерных сооружений и заграждений.
    /// Включает: проволоку, противотанковые ежи, мосты и другие сооружения.
    /// </summary>
    Engineering,

    /// <summary>
    /// Вкладка исследований способностей. Открывается после прохождения уровня M.
    /// Содержит глобальные тактические способности для использования на уровнях.
    /// Включает: артподдержку, газовые атаки, ремонт штаба и другие способности.
    /// </summary>
    Abilities
}
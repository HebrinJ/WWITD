/// <summary>
/// Перечисление состояний узла исследования в древе технологий.
/// Определяет текущий статус узла и его доступность для взаимодействия игроком.
/// Используется системой <see cref="ResearchManager"/> для управления логикой исследований.
/// </summary>
public enum ResearchNodeState
{
    /// <summary>
    /// Узел заблокирован - требования для исследования не выполнены.
    /// Игрок не может взаимодействовать с узлом.
    /// Требования могут включать: непройденные предыдущие узлы, недостаточный уровень игрока и т.д.
    /// Визуально отображается серым/затемненным цветом.
    /// </summary>
    Locked,

    /// <summary>
    /// Узел доступен для исследования - все требования выполнены, ресурсов достаточно.
    /// Игрок может нажать на кнопку исследования и потратить чертежи.
    /// Визуально отображается выделенным/активным цветом.
    /// </summary>
    Available,

    /// <summary>
    /// Узел уже исследован - игрок получил все преимущества этого исследования.
    /// Эффекты применения активны в игре.
    /// Игрок не может повторно исследовать узел.
    /// Визуально отображается зеленым/завершенным цветом.
    /// </summary>
    Researched
}